#include<stdio.h>
int main()
{
    printf("The size of int is \t\t %d \n", sizeof(int));
    printf("The size of char is \t\t %d \n", sizeof(char));
    printf("The size of signed int is \t%d \n", sizeof(signed int));
    printf("The size of unsigned int is \t %d \n", sizeof(unsigned int));
    printf("The size of short int is \t %d \n", sizeof(short int));
    printf("The size of long int is \t %d \n", sizeof(long int));
    printf("The size of long long int is \t %d \n", sizeof(long long int));
    printf("The size of float is \t\t %d \n", sizeof(float));
}
